package Chaptor_04;

public class _If_02 {
    public static void main(String[] args) {
        int number = 3;
        String a = "";
        switch (number) {
            case 1:
                a = "1번인가";
                break;
            case 2:
                a = " 2번인가";
                break;
            case 3:
                a = "3번인가";
                break;
            case 4:
                a = "4번인가";
                break;
        }
        System.out.println(a);

    }
}

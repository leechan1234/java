package day02;

public class Test03 {

    public static void main(String[] args) {

        // 변수의 선언
        boolean b;
        char c;
        int i;
        double d;

//		System.out.println(b);

        // 값 대입 (초기화)
        b = true;
        c = 'A';
        i = 20;
        d = 3.14;

        System.out.println(b);
        System.out.println(c);
        System.out.println(i);
        System.out.println(d);

        // 선언 + 초기화
        int abc = 100;
        System.out.println(abc);

        int num1 = 10, num2 = 20;
        System.out.println(num1);
        System.out.println(num2);
        System.out.println("================");

        num1 = 300;
        num1 = 500;
        System.out.println(num1);

        System.out.println("================");
        num1 = 10 + 20 + 30 + 40;
        System.out.println(num1);


    }

}

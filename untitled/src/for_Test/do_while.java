public class do_while {
    public static void main(String[] args) {
       int sum = 0;
       int i = 0;

        do {

          sum += i;
            i++;
        }while (i <= 100);
        System.out.println(sum);

        int a =10;
        do {
            System.out.println(a);
        }while (a < 7);
    }
}

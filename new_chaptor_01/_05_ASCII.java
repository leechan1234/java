package new_chaptor_01;

public class _05_ASCII {
    public static void main(String[] args) {
        // 아스키 코드 (ANSI)
        char c = 'A';
        System.out.println(c);
        System.out.println((int) c);

        c = '8';
        System.out.println(c);
        System.out.println((int) c);

        c++;
        System.out.println(c);
        System.out.println((int) c);
    }
}
